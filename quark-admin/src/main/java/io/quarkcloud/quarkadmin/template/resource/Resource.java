package io.quarkcloud.quarkadmin.template.resource;

public class Resource {

    // 资源标题
    public String title;

    // 资源副标题
    public String subTitle;

    // 获取资源标题
    public String getTitle() {
        return this.title;
    }

    // 获取资源副标题
    public String getSubTitle() {
        return this.subTitle;
    }
}
